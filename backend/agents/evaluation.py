from models.evaluation_output import EvaluationOutput
from prompts.evaluation_prompt import EVALUATION_PROMPT


def evaluation_agent(
    llm,
    user_query,
    planner_result,
    tool_result,
    retrieved_context,
    ai_response,
):
    """
    Evaluate the response generated by the Response Agent.
    """

    prompt = f"""
{EVALUATION_PROMPT}

User Query:
{user_query}

Planner Result:
{planner_result}

Tool Result:
{tool_result}

Retrieved Context:
{retrieved_context}

Generated Response:
{ai_response}
"""

    structured_llm = llm.with_structured_output(
        EvaluationOutput
    )

    return structured_llm.invoke(prompt)